### VARIABLES NAMES ###
# As we know in the programming world the variables can have short or descriptive names, but each programming language use some standards
# In this case Python have some multiple standards:
  # Must start with a letter or a underscore -> _variable or variable
  # Can't start with a number
  # Can only contain alpha numeric characters (A-z, 0-9, and _ )
  # The variables can't have '-'
  
### Multi Words Variable Names ###
# In python exists three standards to name our variables:
  # Camel Case ->
myVariable = "This can happen"
  # Pascal Case ->
MyVariable = "This can happen too :D"
  # Snake Case ->
my_variable = "This is awesome as well! :D"